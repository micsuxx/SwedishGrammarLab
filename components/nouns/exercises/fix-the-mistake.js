export function renderFixTheMistakeQuiz() {

    const quizForm = document.querySelector("#gap-quiz");
    const scoreBox = document.querySelector("#quiz-score");
    const resetButton = document.querySelector("#quiz-reset");

    if (!quizForm) return;

    scoreBox.classList.remove("is-visible");
    scoreBox.textContent = "";

    function shuffle(array) {
        return [...array].sort(() => Math.random() - 0.5);
    }

    let questions = shuffle(nounCorrectionQuestions).slice(0, 10);

    function renderQuiz() {

        scoreBox.classList.remove("is-visible");
        scoreBox.textContent = "";

        quizForm.innerHTML = questions.map((item, index) => `
            <section
                class="quiz-item"
                data-question-index="${index}"
            >

                <p class="question">
                    ${index + 1}. ${item.question}
                </p>

                <input
                    type="text"
                    class="correction-input"
                    placeholder="Write the correct sentence..."
                    autocomplete="off"
                >

            </section>
        `).join("");
    }

    renderQuiz();

    quizForm.onsubmit = event => {

        event.preventDefault();

        let score = 0;

        questions.forEach((item, index) => {

            const quizItem = quizForm.querySelector(
                `[data-question-index="${index}"]`
            );

            const input =
                quizItem.querySelector(".correction-input");

            const userAnswer =
                input.value.trim().toLowerCase();

            const correctAnswer =
                item.answer.trim().toLowerCase();

            quizItem.classList.remove(
                "is-correct",
                "is-incorrect"
            );

            quizItem
                .querySelectorAll(".hint")
                .forEach(hint => hint.remove());

            if (userAnswer === correctAnswer) {

                score++;
                quizItem.classList.add("is-correct");

            } else {

                quizItem.classList.add("is-incorrect");

                const hint =
                    document.createElement("p");

                hint.className = "hint";

                hint.innerHTML = item.hint;

                quizItem.appendChild(hint);
            }
        });

        scoreBox.textContent =
            `You scored ${score} out of ${questions.length}.`;

        scoreBox.classList.add("is-visible");

        window.scrollTo({
            top: 0,
            behavior: "smooth"
        });
    };

    if (resetButton) {

        resetButton.onclick = () => {

            questions =
                shuffle(nounCorrectionQuestions).slice(0, 10);

            renderQuiz();

            window.scrollTo({
                top: 0,
                behavior: "smooth"
            });
        };
    }
}