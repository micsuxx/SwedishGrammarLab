export function renderGenitiveQuiz() {
    const quizForm = document.querySelector("#gap-quiz");
    const scoreBox = document.querySelector("#quiz-score");
    const resetButton = document.querySelector("#quiz-reset");

    if (!quizForm) return;

    scoreBox.classList.remove("is-visible");
    scoreBox.textContent = "";

    const questions = [...genitiveQuestions]
        .sort(() => Math.random() - 0.5)
        .slice(0, 6);

    quizForm.innerHTML = questions.map((item, index) => `
        <section class="quiz-item" data-question-index="${index}">
            <p class="question">
                ${index + 1}. ${item.question.replace(
                    "_____",
                    `<input
                        type="text"
                        class="gap-input inline-gap-input-nouns"
                        name="q${index}"
                        autocomplete="off"
                    >`
                )}
            </p>
        </section>
    `).join("");

    quizForm.onsubmit = event => {
        event.preventDefault();

        let score = 0;

        questions.forEach((item, index) => {
            const quizItem = quizForm.querySelector(
                `[data-question-index="${index}"]`
            );

            const input = quizForm.querySelector(
                `input[name="q${index}"]`
            );

            const userAnswer = input.value.trim().toLowerCase();

            quizItem.classList.remove(
                "is-correct",
                "is-incorrect"
            );

            quizItem
                .querySelectorAll(".hint")
                .forEach(hint => hint.remove());

            const correctAnswers = Array.isArray(item.answer)
                ? item.answer.map(answer =>
                    answer.trim().toLowerCase()
                )
                : [item.answer.trim().toLowerCase()];

            if (correctAnswers.includes(userAnswer)) {
                score++;
                quizItem.classList.add("is-correct");
            } else {
                quizItem.classList.add("is-incorrect");

                const hint = document.createElement("p");

                hint.className = "hint";
                hint.innerHTML = item.hint;

                quizItem.appendChild(hint);
            }
        });

        scoreBox.textContent =
            `You scored ${score} out of ${questions.length}.`;

        scoreBox.classList.add("is-visible");

        window.scrollTo({
            top: 0,
            behavior: "smooth"
        });
    };

    if (resetButton) {
        resetButton.onclick = () => {
            renderGenitiveQuiz();

            window.scrollTo({
                top: 0,
                behavior: "smooth"
            });
        };
    }
}