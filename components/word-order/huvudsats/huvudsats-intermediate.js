function shuffle(array) {
    return [...array].sort(() => Math.random() - 0.5);
}

function normalize(sentenceArray) {
    return sentenceArray
        .join(" ")
        .replace(/\s+/g, " ")
        .trim()
        .toLowerCase();
}

export function renderIntermediateHuvudsatsDragQuiz() {
    const quizForm = document.querySelector("#drag-quiz");
    const scoreBox = document.querySelector("#quiz-score");
    const resetButton = document.querySelector("#quiz-reset");

    if (!quizForm) return;

    scoreBox.classList.remove("is-visible");
    scoreBox.textContent = "";

    const questions = shuffle(intermediateHuvudsatsQuestions).slice(0, 6);

    quizForm.innerHTML = questions.map((item, index) => `
        <section class="quiz-item" data-question-index="${index}">
            <p class="question">${index + 1}. Complete the sentence.</p>

            <div class="answer-bank" data-answer-bank="${index}">

                <span class="word-chip locked">${item.starter}</span>

            </div>

            <p class="drag-instruction">Available words</p>

            <div class="word-bank">
                ${shuffle(item.words).map(word => `
                    <button type="button" class="word-chip">${word}</button>
                `).join("")}
            </div>
        </section>
    `).join("");

    quizForm.querySelectorAll(".word-chip:not(.locked)").forEach(chip => {
        chip.addEventListener("click", () => {
            const parent = chip.parentElement;
            const quizItem = chip.closest(".quiz-item");
            const wordBank = quizItem.querySelector(".word-bank");
            const answerBank = quizItem.querySelector(".answer-bank");

            if (parent.classList.contains("word-bank")) {
                answerBank.appendChild(chip);
            } else {
                wordBank.appendChild(chip);
            }
        });
    });

    quizForm.onsubmit = event => {
        event.preventDefault();

        let score = 0;

        questions.forEach((item, index) => {
            const quizItem = quizForm.querySelector(`[data-question-index="${index}"]`);
            const answerBank = quizItem.querySelector(".answer-bank");

            const userAnswer = [...answerBank.querySelectorAll(".word-chip")]
                .map(chip => chip.textContent.trim());

            quizItem.classList.remove("is-correct", "is-incorrect");
            quizItem.querySelectorAll(".hint").forEach(hint => hint.remove());

            if (normalize(userAnswer) === normalize(item.answer)) {
                score++;
                quizItem.classList.add("is-correct");
            } else {
                quizItem.classList.add("is-incorrect");

                const hint = document.createElement("p");
                hint.className = "hint";
                hint.innerHTML = item.hint;

                quizItem.appendChild(hint);
            }
        });

        scoreBox.textContent = `You scored ${score} out of ${questions.length}.`;
        scoreBox.classList.add("is-visible");

        window.scrollTo({
            top: 0,
            behavior: "smooth"
        });
    };

    if (resetButton) {
        resetButton.onclick = () => {
            renderIntermediateHuvudsatsDragQuiz();

            window.scrollTo({
                top: 0,
                behavior: "smooth"
            });
        };
    }
}