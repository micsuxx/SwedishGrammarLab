function getRandomQuestions(amount = 6) {
    return [...bisatsEasyQuestions]
        .sort(() => Math.random() - 0.5)
        .slice(0, amount);
}

export function renderBisatsEasyQuiz() {
    const form = document.querySelector("#bisats-quiz");
    const scoreBox = document.querySelector("#quiz-score");
    const resetButton = document.querySelector("#quiz-reset");

    let currentQuestions = getRandomQuestions();

    function render() {
        form.innerHTML = "";
        scoreBox.textContent = "";

        currentQuestions.forEach((question, questionIndex) => {
            const card = document.createElement("article");
            card.className = "quiz-item";
            card.dataset.questionIndex = questionIndex;

            const title = document.createElement("h2");
            title.textContent = `Question ${questionIndex + 1}`;

            const instruction = document.createElement("p");
            instruction.textContent = "Click all words that belong to the bisats.";

            const wordBox = document.createElement("div");
            wordBox.className = "bisats-word-bank";

            question.words.forEach((word, wordIndex) => {
                const button = document.createElement("button");

                button.type = "button";
                button.className = "bisats-word";
                button.textContent = word;
                button.dataset.question = questionIndex;
                button.dataset.word = wordIndex;

                button.addEventListener("click", () => {
                    button.classList.toggle("selected");
                });

                wordBox.appendChild(button);
            });

            const feedback = document.createElement("p");
            feedback.className = "bisats-feedback";
            feedback.dataset.feedback = questionIndex;

            card.appendChild(title);
            card.appendChild(instruction);
            card.appendChild(wordBox);
            card.appendChild(feedback);

            form.appendChild(card);
        });
    }

    form.onsubmit = event => {
        event.preventDefault();

        let score = 0;

        currentQuestions.forEach((question, questionIndex) => {
            const card = form.querySelector(
                `[data-question-index="${questionIndex}"]`
            );

            const buttons = card.querySelectorAll(".bisats-word");

            const selected = [...buttons]
                .filter(button => button.classList.contains("selected"))
                .map(button => Number(button.dataset.word));

            const correct =
                selected.length === question.answer.length &&
                selected.every(index => question.answer.includes(index));

            card.classList.remove("is-correct", "is-incorrect");
            card.querySelectorAll(".hint").forEach(hint => hint.remove());

            if (correct) {
                score++;
                card.classList.add("is-correct");
            } else {
                card.classList.add("is-incorrect");

                const correctWords = question.answer
                    .map(index => question.words[index])
                    .join(" ");

                const hint = document.createElement("p");
                hint.className = "hint";
                hint.innerHTML = `The bisats is "<b><i>${correctWords}</i></b>"`;

                card.appendChild(hint);
            }
        });

        scoreBox.textContent =
            `You scored ${score} out of ${currentQuestions.length}.`;

        scoreBox.classList.add("is-visible");

        window.scrollTo({
            top: 0,
            behavior: "smooth"
        });
    };

    resetButton.addEventListener("click", () => {
        currentQuestions = getRandomQuestions();
        render();
    });

    render();
}