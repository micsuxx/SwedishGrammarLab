function shuffle(array) {
    return [...array].sort(() => Math.random() - 0.5);
}

export function renderBisatsIntermediateQuiz() {
    const quizForm = document.querySelector("#connector-quiz");
    const scoreBox = document.querySelector("#quiz-score");
    const resetButton = document.querySelector("#quiz-reset");

    if (!quizForm) return;

    scoreBox.classList.remove("is-visible");
    scoreBox.textContent = "";

    const questions = shuffle(connectorQuestions).slice(0, 6);

    quizForm.innerHTML = questions.map((item, index) => `
        <section class="quiz-item" data-question-index="${index}">
            <p class="question">${index + 1}. ${item.sentence}</p>

            <div class="option-bank">
                ${shuffle(item.options).map(option => `
                    <button type="button" class="word-chip" data-answer="${option}">
                        ${option}
                    </button>
                `).join("")}
            </div>
        </section>
    `).join("");

    quizForm.querySelectorAll(".word-chip").forEach(chip => {
        chip.addEventListener("click", () => {
            const optionBank = chip.closest(".option-bank");

            optionBank
                .querySelectorAll(".word-chip")
                .forEach(button =>
                    button.classList.remove("selected")
                );

            chip.classList.add("selected");
        });
    });

    quizForm.onsubmit = event => {
        event.preventDefault();

        let score = 0;

        questions.forEach((item, index) => {
            const quizItem = quizForm.querySelector(
                `[data-question-index="${index}"]`
            );

            const selected = quizItem.querySelector(".word-chip.selected");

            quizItem.classList.remove("is-correct", "is-incorrect");
            quizItem.querySelectorAll(".hint").forEach(hint => hint.remove());

            if (selected && selected.dataset.answer === item.answer) {
                score++;
                quizItem.classList.add("is-correct");
            } else {
                quizItem.classList.add("is-incorrect");

                const hint = document.createElement("p");
                hint.className = "hint";
                hint.innerHTML = `The correct connector is "<b><i>${item.answer}</i></b>".`;

                quizItem.appendChild(hint);
            }
        });

        scoreBox.textContent = `You scored ${score} out of ${questions.length}.`;
        scoreBox.classList.add("is-visible");

        window.scrollTo({
            top: 0,
            behavior: "smooth"
        });
    };

    if (resetButton) {
        resetButton.onclick = () => {
            renderBisatsIntermediateQuiz();

            window.scrollTo({
                top: 0,
                behavior: "smooth"
            });
        };
    }
}